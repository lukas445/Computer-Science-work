mark=int(input('Enter your mark for Computer Science Paper 2:'))
if(mark>=48):
    print('Your grade is an A')
elif(mark>=41):
    print('Your grade is a B')
elif(mark>=34):
    print('Your grade is a C')
elif(mark>=28):
    print('Your grade is a D')
elif(mark>=22):
    print('Your grade is an E')
elif(mark>=0):
    print('Your grade is a U')
elif(mark>=71):
    print('The maximum amount of marks is 70')
elif(mark<=-1):
    print('The minimum amount of marks is 0')
